#import "LTPUITextField.h"


@implementation LTPUITextField

- (CGRect)textRectForBounds:(CGRect)bounds {
    CGRect inset = CGRectMake(bounds.origin.x - 5, bounds.origin.y, bounds.size.width , bounds.size.height);
    return inset;
}

- (CGRect)editingRectForBounds:(CGRect)bounds {
    CGRect inset = CGRectMake(bounds.origin.x - 5, bounds.origin.y, bounds.size.width , bounds.size.height);
    return inset;
}

@end